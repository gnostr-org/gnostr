
def braille_convert(icon_lines):
    braille_lines = []
    for i in range(0, len(icon_lines), 4):
        line = ""
        max_len = 0
        for k in range(4):
            if i + k < len(icon_lines):
                max_len = max(max_len, len(icon_lines[i+k]))
        
        for j in range(0, max_len, 2):
            code = 0
            dots = [
                (0, 0, 0x01), (0, 1, 0x02), (0, 2, 0x04), (1, 0, 0x08),
                (1, 1, 0x10), (1, 2, 0x20), (0, 3, 0x40), (1, 3, 0x80)
            ]
            for dx, dy, bit in dots:
                if i + dy < len(icon_lines):
                    line_str = icon_lines[i + dy]
                    if j + dx < len(line_str):
                        if line_str[j + dx] == '#':
                            code |= bit
            line += chr(0x2800 + code)
        braille_lines.append(line)
    return braille_lines

def upscale_icon(icon_lines, factor=2):
    upscaled = []
    for line in icon_lines:
        new_line = "".join([c * factor for c in line])
        for _ in range(factor):
            upscaled.append(new_line)
    return upscaled

